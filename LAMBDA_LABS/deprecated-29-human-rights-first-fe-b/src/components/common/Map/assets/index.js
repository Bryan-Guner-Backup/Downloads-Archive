function parsedEvent(incident) {
  return {
    geometry: {
      type: 'Point',
      coordinates: [parseFloat(incident.long), parseFloat(incident.lat)],
    },
    type: 'Feature',
    properties: {
      id: incident.id,
      date_text: incident['date'],
      title: incident.title,
      type: incident['tags'],

      link1: incident.src.length ? incident.src[0] : '',
    },
  };
}

